#ifndef _LSTD_TYPES_H_
#define _LSTD_TYPES_H_

/**
 * @defgroup typedefs Standard data types
 * @{
 */

/**
 * @typedef bool_t
 * @brief @brief Type definition for boolean
 */
typedef unsigned char bool_t;

/**
 * @typedef u8_t
 * @brief Type definition for 8-bit unsigned INT
 */
typedef unsigned char u8_t;

/**
 * @typedef s8_t
 * @brief Type definition for 8-bit signed INT
 */
typedef signed char s8_t;

/**
 * @typedef c8_t
 * @brief Type definition for 8-bit char
 */
typedef unsigned char c8_t;

/**
 * @typedef u16_t
 * @brief Type definition for 16-bit unsigned int
 */
typedef unsigned short int u16_t;

/**
 * @typedef s16_t
 * @brief Type definition for 16-bit signed INT
 */
typedef signed short int s16_t;

/**
 * @typedef u32_t
 * @brief Type definition for 32-bit unsigned int
 */
typedef unsigned int u32_t;

/**
 * @typedef s32_t
 * @brief Type definition for 32-bit signed INT
 */
typedef signed int s32_t;

/**
 * @typedef u64_t
 * @brief Type definition for 64-bit unsigned int
 */
typedef unsigned long int u64_t;

/**
 * @typedef s64_t
 * @brief Type definition for 64-bit signed int
 */
typedef signed long int s64_t;

/**
 * @typedef fl32_t
 * @brief Type definition for 32-bit float
 */
typedef float fl32_t;

/**
 * @typedef fl64_t
 * @brief Type definition for 64-bit float
 */
typedef double fl64_t;

/** @} */

#endif /* _LSTD_TYPES_H_ */